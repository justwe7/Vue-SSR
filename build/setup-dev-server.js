const Webpack = require('webpack')
const fs = require('fs')
const path = require('path')
const chokidar = require('chokidar')
const { readFile, resolve } = require('./utils')
const cp = require('child_process')

const webpackDevMiddleware = require('./koa-webpack-dev-middleware')
const webpackHotMiddleware = require('./koa-webpack-hot-middleware')
const clientConfig = require('./webpack.client')

module.exports = function setupDevServer (app, templatePath, cb) {
  let bundle
  let template
  let clientManifest

  let ready
  const readyPromise = new Promise(r => { ready = r })
  const update = () => {
    if (bundle && clientManifest) {
      ready()
      cb(bundle, {
        template,
        clientManifest
      })
    }
  }

  // read template from disk and watch
  template = fs.readFileSync(templatePath, 'utf-8')
  chokidar.watch(templatePath).on('change', () => {
    template = fs.readFileSync(templatePath, 'utf-8')
    console.log('index.html template updated.')
    update()
  })

  // modify client config to work with hot middleware
  // clientConfig.entry.app = [
  //   // resolveCli('./node_modules/webpack-hot-middleware/client'),
  //   'webpack-hot-middleware/client',
  //   ...clientConfig.entry.bundle
  // ]
  // clientConfig.output.filename = '[name].js'
  clientConfig.plugins.push(
    new Webpack.HotModuleReplacementPlugin(),
    new Webpack.NoEmitOnErrorsPlugin()
  )

  // dev middleware
  const clientCompiler = Webpack(clientConfig)
  const devMiddleware = webpackDevMiddleware(clientCompiler, {
    publicPath: clientConfig.output.publicPath,
    stats: {
      colors: true,
      assets: false,
      chunks: false,
      modules: false
    }
  })
  app.use(devMiddleware)
  clientCompiler.hooks.done.tap('done', stats => {
    stats = stats.toJson()
    if (stats.errors.length) return

    clientManifest = JSON.parse(readFile(
      devMiddleware.fileSystem,
      'vue-ssr-client-manifest.json',
      clientConfig.output.path
    ))
    update()
  })

  // hot middleware
  app.use(webpackHotMiddleware(clientCompiler, { heartbeat: 5000, log: false }))

  // watch and update server renderer
  // const serverCompiler = Webpack(serverConfig)
  // const mfs = new MFS()
  // serverCompiler.outputFileSystem = mfs
  // serverCompiler.watch({}, (err, stats) => {
  //   if (err) throw err
  //   stats = stats.toJson()
  //   // stats为编译过的文件
  //   if (stats.errors.length) return
  //   // read bundle generated by vue-ssr-webpack-plugin
  //   bundle = JSON.parse(readFile(mfs, 'vue-ssr-server-bundle.json'))
  //   update()
  // })
  // watch and update server renderer

  // // fork了一个子进程
  const child = cp.fork(path.join(__dirname, './server-compiler.js'), {
    cwd: resolve('./')
  })

  child.on('message', res => {
    switch (res.type) {
      case 'bundle':
        // res.data即为bundle信息
        bundle = res.data
        update()
        break
      case 'log':
        console.log(res.data)
        break
      default:
        if (res.data) {
          if (res.data.length) {
            console.log('----- 💣 server compiler error 💣 -----')
            console.log('\x1B[31m' + '%s' + '\x1B[39m', ...res.data)
          } else {
            console.error(res.data)
          }
        } else {
          console.error(res)
        }
    }
  })
  child.on('error', err => {
    console.log('server build error:', err)
  })

  return { readyPromise, fs: devMiddleware.fileSystem }
}
